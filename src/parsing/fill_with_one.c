void	fill_with_one(char **map)
{
	int	i;
	int	j;

	j = 0;
	while (map[j])
	{
		i = 0;
		while (map[j][i])
		{
			if (map[j][i] == ' ')
				map[j][i] = '1';
			i++;
		}
		j++;
	}
}
